from datetime import datetime

def create_file_summary(file_name: str, summary_content: str) -> str:
    """
    Print a structured summary for a data file.
    
    This tool creates a standardized summary with:
    - Clear header indicating this is a summary, not the original file
    - Timestamp of when the summary was created
    - The file name being summarized
    - The summary content provided
    
    Use this after calling preview_data_file() to document what's in the file.
    
    Args:
        file_name: Name of the file being summarized (e.g., "data.xlsx" or "triangles.csv")
        summary_content: The summary text describing the file structure, columns, data characteristics, and purpose
    
    Returns:
        The formatted summary text (also prints to console).
    
    Example:
        create_file_summary(
            file_name="claims_data.xlsx",
            summary_content="Excel file with 3 sheets (HMO, PPO, POS). Contains loss triangles with columns for accident year and development periods. Used for reserve analysis."
        )
    """
    
    # Get current timestamp
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    # Create structured summary content
    structured_content = f"""# FILE SUMMARY

**This is a summary document, not the original data file.**

## File Information
- **File Name:** {file_name}
- **Summary Created:** {timestamp}
- **Location:** {file_name}

## Summary

{summary_content}

---
*This summary was automatically generated by TeamAnalyst to provide quick reference without opening the full data file.*
"""
    
    # Print the summary to console
    print(structured_content)
    
    # Return the summary
    return structured_content
